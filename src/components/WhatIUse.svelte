<h3>Frontend</h3>
<ul>
	<li>
		<a href="https://react.dev/learn/describing-the-ui">React</a> is my library of choice for heavier
		projects;
	</li>
	<li>
		<a href="https://svelte.dev/docs/introduction">Svelte</a> is my framework of choice for lighter ones;
	</li>
	<li>
		<a href="https://www.typescriptlang.org/docs/handbook/typescript-in-5-minutes.html"
			>Typescript</a
		> is amazing (typed > untyped);
	</li>
	<li>
		<a href="https://github.com/pmndrs/zustand">Zustand</a> and
		<a href="https://jotai.org/">Jotai</a>
		are tied for my current favorite State Management library at the moment.
	</li>
	<li>
		<a href="https://cssinjs.org/?v=v10.10.0">JSS</a> is my current favorite styling library for
		SPAs, though I prefer good old <a href="https://sass-lang.com/guide/">Sass</a> for design systems;
	</li>
</ul>

<h3>Backend</h3>
<ul>
	<li>
		<a href="https://docs.python.org/3/faq/general.html#why-is-it-called-python">Python</a> is nice for
		smaller applications, and thanks to type hinting is increasingly plausible for larger projects;
	</li>
	<li>
		<a href="https://en.wiktionary.org/wiki/C%2B%2B#Etymology">C++</a> is good fun for computation-heavy
		hobby projects;
	</li>
	<li>
		<a href="https://www.postgresql.org/about/">PostgreSQL</a> is my preferred DB (yay for open-source);
	</li>
</ul>

<h3>Deployment</h3>
<ul>
	<li>
		<a href="https://github.com/mit1mit1/mit1mit1.github.io/actions">GitHub Actions</a> evidently do
		the trick for hobby projects, but
		<a href="https://buildkite.com/docs/pipelines/create-your-own">Buildkite</a>
		is my favorite pipeline platform so far;
	</li>
	<li>
		<a href="https://www.terraform.io/use-cases/infrastructure-as-code">Terraform</a> is brilliant for
		infrastructure management;
	</li>
	<li>
		<a href="https://docs.docker.com/get-started/overview/">Docker</a> for getting everything
		working <em>consistently</em>;
	</li>
	<li>
		<a href="https://docs.aws.amazon.com/s3/?icmpid=docs_homepage_featuredsvcs">AWS</a> is the cloud
		platform I use at my current company;
	</li>
</ul>

<h3>Testing and Observability</h3>
<ul>
	<li>
		<a href="https://testing-library.com/docs/guiding-principles/">React Testing Library</a> is brilliant,
		as is Kent C. Dodd's philosophy behind it;
	</li>
	<li>
		<a href="https://www.cypress.io/">Cypress</a> and
		<a href="https://playwright.dev/docs/intro">Playwright</a> are both very worthwhile when kept light
		enough to be quick and reliable;
	</li>
	<li>
		<a href="https://docs.datadoghq.com/monitors/">Datadog</a> is brilliant for metrics and
		monitoring to make sure nothing is going wrong;
		<a href="https://www.heap.io/use-case/user-behavior">Heap</a> is brilliant for metrics to figure
		out whether what you're building is useful;
	</li>
	<li>
		There's always a disclaimer that automated testing is never sufficient to guarantee
		accessibility. That may be so, but damn does <a href="https://www.deque.com/axe/">Axe</a> get you
		a long way towards it;
	</li>
</ul>
